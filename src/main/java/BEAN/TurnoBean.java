package BEAN;

public class TurnoBean {
    private int     CODTURNO;
    private String  NOMBTURNO;
    private String  ESTADO;
    private int     OFFSPRING;

    public int getCODTURNO() {
        return CODTURNO;
    }

    public void setCODTURNO(int CODTURNO) {
        this.CODTURNO = CODTURNO;
    }

    public String getNOMBTURNO() {
        return NOMBTURNO;
    }

    public void setNOMBTURNO(String NOMBTURNO) {
        this.NOMBTURNO = NOMBTURNO;
    }

    public String getESTADO() {
        return ESTADO;
    }

    public void setESTADO(String ESTADO) {
        this.ESTADO = ESTADO;
    }

    public int getOFFSPRING() {
        return OFFSPRING;
    }

    public void setOFFSPRING(int OFFSPRING) {
        this.OFFSPRING = OFFSPRING;
    }
    
}
