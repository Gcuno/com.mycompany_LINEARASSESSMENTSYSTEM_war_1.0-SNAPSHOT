package BEAN;

public class DiaBean {
    private int     CODDIA;
    private String  NOMBDIA;
    private String  ESTADO;
    private int     OFFSPRING;

    public int getCODDIA() {
        return CODDIA;
    }

    public void setCODDIA(int CODDIA) {
        this.CODDIA = CODDIA;
    }

    public String getNOMBDIA() {
        return NOMBDIA;
    }

    public void setNOMBDIA(String NOMBDIA) {
        this.NOMBDIA = NOMBDIA;
    }

    public String getESTADO() {
        return ESTADO;
    }

    public void setESTADO(String ESTADO) {
        this.ESTADO = ESTADO;
    }

    public int getOFFSPRING() {
        return OFFSPRING;
    }

    public void setOFFSPRING(int OFFSPRING) {
        this.OFFSPRING = OFFSPRING;
    }
    
}
