package BEAN;

public class LaboratorioBean {
    private int     CODLAB;
    private String  DESCRIPLAB;
    private int     NROPCSLAB;
    private String  ESTADO;
    private int     OFFSPRING;

    public int getCODLAB() {
        return CODLAB;
    }

    public void setCODLAB(int CODLAB) {
        this.CODLAB = CODLAB;
    }

    public String getDESCRIPLAB() {
        return DESCRIPLAB;
    }

    public void setDESCRIPLAB(String DESCRIPLAB) {
        this.DESCRIPLAB = DESCRIPLAB;
    }

    public int getNROPCSLAB() {
        return NROPCSLAB;
    }

    public void setNROPCSLAB(int NROPCSLAB) {
        this.NROPCSLAB = NROPCSLAB;
    }

    public String getESTADO() {
        return ESTADO;
    }

    public void setESTADO(String ESTADO) {
        this.ESTADO = ESTADO;
    }

    public int getOFFSPRING() {
        return OFFSPRING;
    }

    public void setOFFSPRING(int OFFSPRING) {
        this.OFFSPRING = OFFSPRING;
    }
    
}
